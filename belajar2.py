#operasi komparasi 
# setiap hasil dari operasi komparasi adalah boolean

a=4
b=2
# lebih dari (>)
print ("=========== lebih dari (>)")
hasil= a > 3
print (a,'>',3,hasil)
hasil= 2 > 3
print (2,'>',3,hasil)

# kurang dari (<)
print ("=========== kurang dari (<)")
hasil= a < 3
print (a,'<',3,hasil)
hasil= 2 < 3
print (2,'<',3,hasil)

# lebih dari sama dengan (>=)
print ("=========== lebih dari sama dengan (>=)")
hasil= a >= 3
print (a,'>=',3,hasil)
hasil= 2 >= 3
print (2,'>=',3,hasil)
hasil= a >= 4
print (a,'>=',4,hasil)

# kurang dari sama dengan (<=)
print ("=========== kurang dari sama dengan (<=)")
hasil= a <= 3
print (a,'<=',3,hasil)
hasil= 2 <= 3
print (2,'<=',3,hasil)
hasil= a <= 4
print (a,'<=',4,hasil)

#  sama dengan (==) / adapun juga tidak sama dengan(!=)
print ("===========  sama dengan (==)")
hasil= a == 3
print (a,'==',3,hasil)
hasil= 2 == 3
print (2,'==',3,hasil)
hasil= a == 4
print (a,'==',4,hasil)

